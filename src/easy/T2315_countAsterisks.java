package easy;

public class T2315_countAsterisks {
    public int countAsterisks(String s) {
        String[] split = s.split("\\|");
        int result = 0;
        for (int i = 0; i < split.length; i += 2) {
            if (split[i].contains("*")) {
                result += count(split[i]);
            }
        }
        return result;
    }

    public int count(String s){
        int result = 0;
        for (int j = 0; j <s.length(); j++) {
            if (s.charAt(j) == '*') {
                result++;
            }
        }
        return result;
    }

    public static void main(String[] args) {
        System.out.println(new T2315_countAsterisks().countAsterisks("|**************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************|"));
    }
}
